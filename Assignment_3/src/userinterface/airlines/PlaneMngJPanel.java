    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)tblPlaneInfo.getModel();
        dtm.setRowCount(0);
        
        for(Plane p : fleet.getPlaneList()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = p;
            row[1]= p.getModel();
            dtm.addRow(row);
        }
    }
