    public void populate(){
        DefaultTableModel dtm = (DefaultTableModel) tblFlight.getModel();
        dtm.setRowCount(0);
        
        for(Flight flight : flightCatalog.getFlightList()){
            for(Fleet fleet : airlines.getFleetList())
                if(fleet.getPlaneList().contains(flight.getPlane())){
                    Object[] row = new Object[dtm.getColumnCount()];
                    row[0] = flight;
                    row[1] = fleet.getFleetName();
                    DateFormat df = new SimpleDateFormat("MM/dd/yy HH:mm");
                    row[2] = df.format(flight.getDepartureDate());
                    dtm.addRow(row);
                }
        }
    }
    
    

